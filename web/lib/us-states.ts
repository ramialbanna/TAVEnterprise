export type UsStateCode = (typeof US_STATES)[number]["code"];

const STATE_CODE_SET = new Set<string>(US_STATES.map((s) => s.code));

/** True when `value` is a known 2-letter state code. */
export function isKnownUsStateCode(value: string): value is UsStateCode {
  return STATE_CODE_SET.has(value);
}

/**
 * Map a stored title/tag state value to a select value.
 * Accepts 2-letter codes or full state names; returns the code when recognized.
 * Legacy free-text that does not match a state is returned unchanged for display.
 */
export function normalizeStoredUsState(raw: string | null | undefined): string {
  const trimmed = raw?.trim() ?? "";
  if (!trimmed) return "";

  const upper = trimmed.toUpperCase();
  if (STATE_CODE_SET.has(upper)) return upper;

  const byName = US_STATES.find((s) => s.name.toLowerCase() === trimmed.toLowerCase());
  if (byName) return byName.code;

  return trimmed;
}
